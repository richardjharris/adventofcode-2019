from itertools import islice

def split_every(n, iterable):
    i = iter(iterable)
    piece = list(islice(i, n))
    while piece:
        yield piece
        piece = list(islice(i, n))

def slurp(filename):
    with open(filename, 'r') as fh:
        return fh.read().rstrip('\n')

